'use strict';

var gulp = require('gulp');
var $ = require('gulp-load-plugins')();
var rimraf = require('rimraf');
var browserSync = require('browser-sync');
var runSequence = require('run-sequence');
var karma = require('karma').server;

var onError = function (err) {
  $.util.beep();
  console.log(err);
};

// Settings
var BUILD = './build';
var DIST = './dist';
var SRC = './src';


// DEVELOPMENT TASKS
//================================================

/*
* 1. Setup a webserver with livereload using BrowserSync
* 2. JS and CSS get processed and served from the 'build' folder
* 3. JSX: Transform jsx React files and put in build 'build' folder
* 4. Compile sass files, autoprefix and put in 'build' folder
* */

 // BrowserSync Server
gulp.task('browser-sync', function() {
  browserSync.init([
    BUILD + '/css/*.css',
    BUILD + '/js/**/*.js',
    './**/*.html'
  ],
  {
    notify: false,
    server: {
      baseDir: [BUILD]
    },
    port: 3500,
    browser: [],
    tunnel: false
  });
});

// JSX
gulp.task('jsx', function() {
  return gulp.src(SRC + '/**/*.js')
    .pipe($.cached('jsx'))  //Process only changed files
    .pipe($.react())
    .pipe(gulp.dest('build/'));
});

// Sass
gulp.task('sass', function() {
  return gulp.src(SRC + '/sass/main.scss')
    .pipe($.plumber({
        errorHandler: onError
      }))
    .pipe($.sourcemaps.init())
    .pipe($.sass())
    .pipe($.sourcemaps.write({includeContent: false}))
    .pipe($.sourcemaps.init({loadMaps: true})) // Load sourcemaps generated by sass
    .pipe($.autoprefixer({
      browsers: ['last 2 versions']
    }))
    .pipe($.sourcemaps.write('.'))
    .pipe($.plumber.stop())
    .pipe(gulp.dest('./build/css'));
});

// HTML pages
gulp.task('pages', function() {
  var render = $.render({
      template: './src/templates/index.html',
      data: {title: 'Page Title'},
      harmony: true
    })
    .on('error', onError);

  return gulp.src(BUILD + '/js/main.js')
    .pipe(render)
    .pipe(gulp.dest(BUILD));
});

// serve task
gulp.task('serve', ['browser-sync', 'jsx', 'sass'] , function(cb) {

  $.watch(
    SRC + '/sass/**/*.scss',
    {
      name: 'SASS'
    },
    function() {
      gulp.start('sass');
    }
  );

  $.watch(
    SRC + '/js/**/*.js',
    {
      name: 'JS'
    },
    function() {
      gulp.start('jsx');
    }
  );
});

// Delete build Directory
gulp.task('delete-build', function() {
  rimraf(BUILD, onError);
});

//build (no server)
gulp.task('build', ['jsx', 'sass']);

// Default
gulp.task('default', ['serve']);

// Tests
gulp.task('test', function(done) {
  karma.start({
    configFile: __dirname + '/karma.conf.js'
  }, done);
});


// DISTRIBUTION TASKS
//===============================================

// Delete dist Directory
gulp.task('delete-dist', function() {
  rimraf(DIST, onError);
});

// CSS
gulp.task('css', function() {
  return gulp.src(BUILD + '/css/main.css')
    .pipe($.plumber({
      errorHandler: onError
    }))
    .pipe(gulp.dest(DIST + '/css'))
    .pipe($.csso())
    .pipe($.rename('main.min.css'))
    .pipe($.plumber.stop())
    .pipe(gulp.dest(DIST + '/css'));
});

// Copy index.html to 'dist'
gulp.task('html', function() {
  gulp.src(['./index.html'])
    .pipe(gulp.dest(DIST))
    .on('error', onError);
});

// Bundle with jspm
gulp.task('bundle', ['jsx'], $.shell.task([
  'jspm bundle-sfx build/js/main dist/js/app.js'
]));

// Uglify the bundle
gulp.task('uglify', function() {
  return gulp.src(DIST + '/js/app.js')
    .pipe($.plumber({
      errorHandler: onError
    }))
    .pipe($.sourcemaps.init({loadMaps: true}))
    .pipe($.uglify())
    .pipe($.sourcemaps.write('.'))
    .pipe($.rename('app.min.js'))
    .pipe($.plumber.stop())
    .pipe(gulp.dest(DIST + '/js'));
});

gulp.task('dist', function() {
  runSequence(
    'delete-dist',
    'build',
    ['css', 'html', 'bundle'],
    'uglify'
  );
});